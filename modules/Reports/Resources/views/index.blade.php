<script>
function downloadReportsAsPDF() {
    const downloadBtn = document.getElementById('downloadPdfBtn');
    const originalContent = downloadBtn.innerHTML;
    
    // Show loading state
    downloadBtn.innerHTML = '<i class="ni ni-settings-gear-65 fa-spin"></i> {{ __("Preparing PDF...") }}';
    downloadBtn.disabled = true;
    
    // Add print header with current timestamp and company info
    const printHeader = document.createElement('div');
    printHeader.className = 'print-header';
    printHeader.innerHTML = `
        <h1>{{ __('Reports & Analytics') }}</h1>
        <p><strong>{{ __('Generated on') }}:</strong> ${new Date().toLocaleString()}</p>
        <p><strong>{{ __('Company') }}:</strong> {{ auth()->user()->currentCompany()->name ?? 'N/A' }}</p>
        <p><strong>{{ __('Generated by') }}:</strong> {{ auth()->user()->name }}</p>
    `;
    
    // Insert at the beginning of the container
    const container = document.querySelector('.container-fluid');
    if (container) {
        container.insertBefore(printHeader, container.firstChild);
    }
    
    // Wait for charts to fully render and data to load
    setTimeout(() => {
        // Show user instruction
        if (confirm('{{ __("This will open your browser\'s print dialog. Please select \'Save as PDF\' as your destination.") }}')) {
            // Trigger browser print dialog
            window.print();
        }
        
        // Reset button state
        downloadBtn.innerHTML = originalContent;
        downloadBtn.disabled = false;
        
        // Remove print header after printing
        setTimeout(() => {
            if (printHeader && printHeader.parentNode) {
                printHeader.parentNode.removeChild(printHeader);
            }
        }, 1000);
    }, 1000);
}

// Add keyboard shortcut for PDF download
document.addEventListener('keydown', function(e) {
    if ((e.ctrlKey || e.metaKey) && e.key === 'p') {
        e.preventDefault();
        downloadReportsAsPDF();
    }
});

// Add event listener for after print
window.addEventListener('afterprint', function() {
    // Show success message
    if (typeof js !== 'undefined' && js.notify) {
        js.notify('{{ __("PDF generation completed!") }}', 'success');
    } else {
        alert('{{ __("PDF generation completed!") }}');
    }
});

// Add event listener for before print to ensure all data is loaded
window.addEventListener('beforeprint', function() {
    console.log('Preparing reports for PDF generation...');
});
</script>

